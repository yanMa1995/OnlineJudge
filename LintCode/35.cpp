/**
 * Definition of ListNode
 * 
 * class ListNode {
 * public:
 *     int val;
 *     ListNode *next;
 * 
 *     ListNode(int val) {
 *         this->val = val;
 *         this->next = NULL;
 *     }
 * }
 */
class Solution {
public:
    /**
     * @param head: The first node of linked list.
     * @return: The new head of reversed linked list.
     */
    ListNode *reverse(ListNode *head) {
        // write your code here
        if (head == NULL)   return head;
        
        ListNode *q = head, *pre = NULL, *n = NULL;

        while (q != NULL) {
            n = q->next;
            q->next = pre;
            pre = q;
            q = n;
        }
        return pre;
    }
};
